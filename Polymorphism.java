//polymorphism=poly(many)+morphism(form)
//one name and its multiple form is called polymorphism.
//using polymorphism using single component we can perform more than 1 operation.
//for example-> key of mobile phone, using single key of mobile phone we can type multiple alphabtes.
// two types 1.compile time polymorphism 2.run time polymorphism
// note: all the concept of polymorphism are implemented context of method /constructor.
// program for compile time polymorphism by operator overloading.
public class Polymorphism {
    public static void main(String[] args) {
        int a=10,b=20;
        String c="10",d="20";
        System.out.println(a+b);
        System.out.println(c+d);
        System.out.println(a-b);
        System.out.println(-(a-b));
    }
}
