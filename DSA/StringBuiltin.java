package DSA;
//some built in methods of string in java.
public class StringBuiltin {
    public static void main(String[] args) {
        String s="Shoaib Akhtar";
        System.out.println(s.lastIndexOf('m'));
        System.out.println(s.toLowerCase());
        System.out.println(s.toUpperCase());
        System.out.println(s.startsWith("mar"));
        System.out.println(s.contains("tar"));
        System.out.println(s.endsWith("tar"));
    }
}
